'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''

print(1234)

# Aspas simples
print('Jessica Ferreira')
print(1, 'Jessica "Ferreira"')

# Aspas duplas
print("Jessica Ferreira")
print(2, "Jessica 'Ferreira'")

# Escape
print("Jessica \"Ferreira\"")

# r
print(r"Jessica \"Ferreira\"")
